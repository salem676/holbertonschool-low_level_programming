#include <stdio.h>
/**
 * mul - multiplicates two numbers a and b.
 * @a: first number.
 * @b: second number.
 * Description: integer function which returns product of
 * two given numbers.
 * Return: a*b product.
 */
int mul(int a, int b)
{
	return (a * b);
}
